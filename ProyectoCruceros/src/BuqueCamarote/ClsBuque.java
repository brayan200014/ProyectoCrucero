
package BuqueCamarote;

public class ClsBuque {
    
    public static String descripcion;
    public static int camarotes;
    public static int niveles;
    public static int personas;
    public static int tipoBuque;
    public static int estado;

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public int getCamarotes() {
        return camarotes;
    }

    public void setCamarotes(int camarotes) {
        this.camarotes = camarotes;
    }

    public int getNiveles() {
        return niveles;
    }

    public void setNiveles(int niveles) {
        this.niveles = niveles;
    }

    public int getPersonas() {
        return personas;
    }

    public void setPersonas(int personas) {
        this.personas = personas;
    }

    public int getTipoBuque() {
        return tipoBuque;
    }

    public void setTipoBuque(int tipoBuque) {
        this.tipoBuque = tipoBuque;
    }

    public int getEstado() {
        return estado;
    }

    public void setEstado(int estado) {
        this.estado = estado;
    }
    
    
    
    
    
}
